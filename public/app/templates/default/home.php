                    <li><b>DO</b> support the person’s breathing by administering oxygen or performing rescue breathing.</li>
                    <li><b>DO</b> administer naloxone.</li>
                    <li><b>DO</b> put the person in the “recovery position” on the side, if he or she is breathing independently.</li>
